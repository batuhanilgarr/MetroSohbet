// Tüm istasyonları tek bir listede topla
export const allStations = metroLines.flatMap(line => line.stations)

// İstasyon adından hangi hatlarda bulunduğunu bul
export function getLinesForStation(stationName: string): string[] {
  return metroLines
    .filter(line => line.stations.includes(stationName))
    .map(line => line.name)
}

// Hat adından istasyonları getir
export function getStationsForLine(lineName: string): string[] {
  const line = metroLines.find(l => l.name === lineName)
  return line?.stations || []
}
